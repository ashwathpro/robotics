function output_img=autocolor(input_img)
%==========================================================================
a= rgb2ntsc(input_img);
[m1 n1 r1]=size(a);
%==========================================================================
%==========================================================================
limit=mean(mean(a(:,:,2)));
my_limit=0.1;
limca=my_limit-limit;
%==========================================================================
for(i=1:m1)
    for(j=1:n1)
          a(i,j,2)=a(i,j,2)+limca;
          if(a(i,j,2)>1)
              a(i,j,2)=1;
          end
          if(a(i,j,2)<-1)
              a(i,j,2)=-1;
          end
      end
end
%==========================================================================
%==========================================================================
limit=mean(mean(a(:,:,3)));
my_limit=0.035;
limca=my_limit-limit;
%==========================================================================
for(i=1:m1)
    for(j=1:n1)
          a(i,j,3)=a(i,j,3)+limca;
          if(a(i,j,3)>1)
              a(i,j,3)=1;
          end
          if(a(i,j,3)<-1)
              a(i,j,3)=-1;
          end
      end
end
%==========================================================================
img=ntsc2rgb(a);
img=img.*255;
output_img=uint8(img);
%==========================================================================